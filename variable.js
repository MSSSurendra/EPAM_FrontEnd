var name = "bhAAi";
console.log(name); // Output: gopi

// Reassigning a var variable
name = "lasya";
console.log(name); // Output: lasya

// Using let
let age = 25;
console.log(age); // Output: 25

// Reassigning a let variable
age = 30;
console.log(age); // Output: 30

// Using const  
var birthYear = 1998;
console.log(birthYear); // Output: 1998

// Attempting to reassign a const variable will cause an error
 birthYear = 2000;